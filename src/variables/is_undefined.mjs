/**
 * Проверяет, имеет ли переменная значение `undefined`
 *
 * @param {any} value Проверяемая переменная.
 * @returns {boolean} Возвращает `true`, если переменная `value` имеет значение `undefined`, иначе `false`.
 */
export default function is_undefined(value) {
    return value === undefined;
}
